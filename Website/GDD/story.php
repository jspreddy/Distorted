            <h2><a name="game">Game World</a></h2>
            <h3>
                General look and feel of world</h3>
            <p>
                Two timelines are present in the game at all times but both contain a dark atmosphere due to the time of night and the nature of the house.</p>
            <p>
                Timeline 1: Steampunk</p>
            <p>
                Timeline 2: 21<sup>st</sup> century</p>
            <h3>
                Area #1</h3>
            <p>
                Basement/Dungeon</p>
            <h4>
                General Description</h4>
            <p>
                The basement is only partially explored in the original timelines. Hidden passageways have some original items from the 15<sup>th</sup> century including some of the torture devices. Each time period has engines and machinery that are consistent with their timeline.</p>
            <h4>
                Physical Characteristics</h4>
            <p>
                Dark, Dank, Stone and Dirt in some places.</p>
            <h4>
                Levels that use area</h4>
            <p>
                Level 1 uses this area as the starting level for the game.</p>
            <h4>
                Connections to other areas</h4>
            <p>
                Upon finishing this level they exit to a hidden alcove that transitions to a cut scene that progresses to Level 2: Main floor.</p>
            <p>
                &nbsp;</p>

