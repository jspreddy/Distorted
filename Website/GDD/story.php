            <h2><a name="characters">Characters</a></h2>
            <h3>
                Character #1</h3>
            <p>
                Desmond De Retz</p>
            <h4>
                Back story</h4>
            <p>
                It is the 15<sup>th</sup> century and a man by the name of Desmond De Retz is from a family of wealth and power. After completing a successful career in the military he retires to his mansion in western France were he un-laboriously performs social duties but he is unable to undo some of the horrors from the military. Eventually the isolation and lack of activity leads him to invent his own excitement in the form of kidnapping and murdering women. This eventually devolves into him committing many murders and recording them into a journal. The task of filling the journal becomes his only goal in life and his grasp on reality disappears.</p>
            <h4>
                Personality</h4>
            <p>
                Schizophrenic, Duty bound, Powerful, Arrogant, Rich</p>
            <h4>
                Look</h4>
            <p>
                Older gentlemen, early in the game clean cut and established. Slowly his looks become more ragged and wily as his grasp on reality disappears.</p>
            <h4>
                Physical characteristics</h4>
            <p>
                None</p>
            <h4>
                Animations</h4>
            <p>
                Attacking, Walking</p>
            <h4>
                Special Abilities</h4>
            <p>
                Desmond is able to subdue a survivor with enough damage.</p>
            <h4>
                Relevance to game story</h4>
            <p>
                Desmond is the main Antagonist of the story.</p>
            <h4>
                Relationship to other characters</h4>
            <p>
                He is the embodiment of the possessed house in which they are currently the owner of.</p>
            <h4>
                Statistics</h4>
            <p>
                He is a ghost that when the house has acquired enough strength he is able to manifest and attack the players. He has no weapons while manifested but as the house he can cause fear in the players.</p>
