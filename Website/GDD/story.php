                Back story</h3>
            <p>
                It is the 15<sup>th</sup> century and a man by the name of Desmond De Retz is from a family of wealth and power. He takes advantage of this power and starts exploiting his sadistic nature on the surrounding villages engaging in a series of female murders. After some time the villages slowly figure out that he is the source of their despair but because of his power they cannot go through normal methods. Many years passed and nothing is resolved in fact it became worse so the villagers went on a hunt.</p>
            <p>
                Breaking into Desmond&rsquo;s castle they found him in the act in the basement and in their rage decided to express their anger then and there. Desmond was strung up using his own tools and brutally killed by the town people. During his slow sentencing he never screamed he only laughed and cursed the people that his work is not done. He will never die until he has filled every page in his journal so that he can remember every moment in detail in the afterlife. Upon his death the town folks cleaned out the castle and it sat abandoned for many years, until the memory of Desmond faded. It was then that mysterious stories of the castle seemingly alive every April 29<sup>th </sup>began to spread. Though with time no one realized that it was the day of his death and he was still attempting to fill his journal.</p>
